public class SwitchDemo {
    public static void main(String[] args) {
        int demo = 5;
        switch (demo){
            case 1:
                System.out.println("星期一");
                break;
            case 2:
                System.out.println("星期2");
                break;
            case 3:
                System.out.println("星期3");
                break;
            case 4:
                System.out.println("星期4");
                break;
            default:
                System.out.println("最后一天");
        }

    }
}
